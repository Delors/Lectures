
class Patient {
    String geburtsdatum;
    String name;
    int groesse; // in Zentimeter
    double gewicht; // in Kilogramm

    Patient(String geburtsdatum, String name, int groesse, double gewicht) {
        this.geburtsdatum = geburtsdatum;
        this.name = name;
        this.groesse = groesse;
        this.gewicht = gewicht;
    }
}



